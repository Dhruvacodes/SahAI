#!/usr/bin/env node
/**
 * Bundles the protocol catalog JSON into a single TypeScript module the mobile
 * app can import directly. Metro's bundler refuses to resolve paths outside
 * the app's package root, so we generate `apps/mobile/src/protocol/catalog.gen.ts`
 * with all rule JSON inlined.
 *
 * Usage:
 *   node sahai/scripts/build-protocols.mjs
 *
 * Re-run any time you add or edit a rule file.
 */

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, "..");
const CATALOG_DIR = join(REPO_ROOT, "protocols", "v1");
const OUT_PATH = join(
  REPO_ROOT,
  "apps",
  "mobile",
  "src",
  "protocol",
  "catalog.gen.ts",
);

function readJson(path) {
  return JSON.parse(readFileSync(path, "utf8"));
}

const manifest = readJson(join(CATALOG_DIR, "manifest.json"));
const ttt = readJson(join(CATALOG_DIR, "ttt.v1.json"));

const ruleFiles = readdirSync(CATALOG_DIR).filter(
  (f) => f.endsWith(".v1.json") && f !== "ttt.v1.json",
);

const rulesByVertical = {};
const allRules = [];
for (const file of ruleFiles) {
  const payload = readJson(join(CATALOG_DIR, file));
  const vertical = payload.vertical ?? file.replace(/\.v1\.json$/, "");
  rulesByVertical[vertical] = payload.rules ?? [];
  for (const rule of payload.rules ?? []) {
    allRules.push(rule);
  }
}

mkdirSync(dirname(OUT_PATH), { recursive: true });

const banner = `/**
 * AUTO-GENERATED by sahai/scripts/build-protocols.mjs.
 * Do not edit by hand; regenerate after editing protocols/v1/*.json.
 */
/* eslint-disable */
`;

const body = `export const PROTOCOL_MANIFEST = ${JSON.stringify(manifest, null, 2)} as const;

export const PROTOCOL_TTT = ${JSON.stringify(ttt, null, 2)} as const;

export const PROTOCOL_RULES = ${JSON.stringify(allRules, null, 2)} as unknown as ProtocolRule[];

export type Severity = "LOW" | "MODERATE" | "HIGH" | "CRITICAL";

export interface ProtocolRule {
  id: string;
  vertical: string;
  label: { en: string; [k: string]: string | undefined };
  applies_to?: Record<string, number | boolean | undefined>;
  trigger: TriggerExpr;
  escalates_to: Severity;
  ttt_minutes?: number;
  first_response_actions?: Array<{
    id: string;
    text: { en: string; [k: string]: string | undefined };
  }>;
  referral_text_templates?: {
    asha_to_phc?: { en: string; [k: string]: string | undefined };
    patient_instruction?: { en: string; [k: string]: string | undefined };
  };
  rationale: string;
  source: { doc: string; section?: string; url?: string };
  version?: string;
}

export type TriggerExpr =
  | { all_of: TriggerExpr[] }
  | { any_of: TriggerExpr[] }
  | { not: TriggerExpr }
  | { [key: string]: unknown };
`;

writeFileSync(OUT_PATH, banner + body, "utf8");

console.log(
  `Wrote ${OUT_PATH} (manifest v${manifest.version}, ${allRules.length} rules across ${
    Object.keys(rulesByVertical).length
  } verticals).`,
);
